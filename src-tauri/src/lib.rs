// ============================================================
// Здесь определяются Tauri-команды, которые можно вызывать
// из JavaScript-фронтенда. Сейчас тут только тестовая команда,
// потом добавим управление sing-box, профилями и т.д.
// ============================================================

/// Тестовая команда — проверяет связь между фронтендом и бэкендом.
/// Вызывается из JavaScript через: await window.__TAURI__.core.invoke("test_backend")
/// Возвращает приветственную строку из Rust.
#[tauri::command]
fn test_backend() -> String {
    // Просто возвращаем строку чтобы убедиться что Rust-бэкенд работает
    String::from("✅ Hello from Rust! Бэкенд работает.")
}

/// Точка запуска Tauri-приложения.
/// Настраивает плагины и регистрирует команды.
#[cfg_attr(mobile, tauri::mobile_entry_point)]
pub fn run() {
    tauri::Builder::default()
        // Плагин для открытия ссылок в системном браузере
        .plugin(tauri_plugin_opener::init())
        // Регистрируем наши команды — фронтенд сможет их вызывать
        // Когда добавим новые команды, их нужно будет добавить сюда
        .invoke_handler(tauri::generate_handler![test_backend])
        // Запускаем приложение с контекстом из tauri.conf.json
        .run(tauri::generate_context!())
        .expect("Ошибка при запуске приложения vlessok");
}
